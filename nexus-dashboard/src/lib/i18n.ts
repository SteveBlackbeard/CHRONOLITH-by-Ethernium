export function interpolate(message: string, vars?: Record<string, string | number>) {
  if (!vars) return message;
  return Object.entries(vars).reduce((acc, [key, value]) => acc.replaceAll(`{${key}}`, String(value)), message);
}

export function tt(dict: Record<string, string>, key: string, fallback: string, vars?: Record<string, string | number>) {
  return interpolate(dict[key] || translations.EN[key] || fallback, vars);
}

