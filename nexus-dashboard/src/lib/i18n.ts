  if (scanMatch) {
    return `${dict['action.scanning'] || 'SCANNING'} ${scanMatch[1]}`;
  }
  const keyMap: Record<string, string> = {
    CRYSTALLIZE: 'action.crystallize',
    AUDIT: 'action.audit',
    SEAL: 'action.seal',
    ACCESS: 'action.access',
  };
